jQuery.validator.addMethod("lettersonly", function(value, element) {
    return this.optional(element) || /^[a-z]+$/i.test(value);
  }, "Letters only please");

$(document).ready(function(){
    $("#userSignup").validate({
        rules:
        {
            firstname:{
                required:true,
                minlength:4,
                maxlength:20,
                lettersonly:true
            },
            lastname:{
                required:true,
                lettersonly:true,
                maxlength:20
            },
            email:{
                required:true,
                email:true
            },
            password:{
                required:true,
                minlength:8
            },
            phonenumber:{
                required:true,
                minlength:10,
                maxlength:10,
                number:true
            },
            cpassword:{
                required:true,
                equalTo:'#password'
            }

        }
    })
})
